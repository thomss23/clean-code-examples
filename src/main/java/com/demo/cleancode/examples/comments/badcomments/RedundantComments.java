package com.demo.cleancode.examples.comments.badcomments;

/**
In this code, the comment "This method calculates the square of a number" is redundant, as it is already clear from the method name and signature that the method calculates the square of a number.
 The comment "Calculate the square of the number" is also redundant, as it simply repeats what the code is doing.
 **/
public class RedundantComments {

    // This method calculates the square of a number
    public int calculateSquare(int number) {
        return number * number; // Calculate the square of the number
    }
}
